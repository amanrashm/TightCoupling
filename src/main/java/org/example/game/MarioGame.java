package org.example.game;

public class MarioGame {
    public void up() {
        System.out.println("Jumping up");
    }

    public void down() {
        System.out.println("Go to the hole");
    }

    public void left() {
        System.out.println("Go back");
    }

    public void right() {
        System.out.println("Accelerate");
    }
}