package org.example.tightCoupling;

public class SuperContra {
    public void up() {
        System.out.println("up");
    }

    public void down() {
        System.out.println("Sit Down");
    }

    public void left() {
        System.out.println("Go back");
    }

    public void right() {
        System.out.println("Shoot a bullet");
    }
}